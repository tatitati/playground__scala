// lists
val colors = List("blue" , "orange", "red")
print(colors(0)) // blue
print(colors(1)) // orange


// sets
val cities = Set("Madrid", "Barcelona", "Madrid", "Barcelona", "Zurich")
print(cities) // Set(Madrid, Barcelona, Zurich)
print(cities(1))

val persons = Map("Australia" -> "Michael", "Spain" -> "Francisco", "Chinese" -> "Jin")
print(persons) // Map(Australia -> Michael, Spain -> Francisco, Chinese -> Jin)
print(persons("Australia")) // Michael